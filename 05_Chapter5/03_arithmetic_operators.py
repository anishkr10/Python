# There are several arithmetic operators in Python that can be used to perform mathematical operations on numbers
# (+, -, *, /, %, **, //)

# % -> Mod Operator: Returns the remainder of a division operation
# Example: 8 % 3 = 2 (8 divided by 3 is 2 with a remainder of 2)

# ** -> Exponentiation Operator: Raises a number to the power of another number
# Example: 2 ** 3 = 8 (2 raised to the power of 3 is 8)

# // -> Floor Division Operator: Returns the quotient of a division operation, rounded down to the nearest integer
# Example: 8 // 3 = 2 (8 divided by 3 is 2.67, but rounded down is 2)

