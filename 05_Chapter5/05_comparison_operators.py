# Equal to Operators (==)

print(12==12) #Output: True
print(14==12) #Output: False

# Not Equal to

print(5!=7) #output: True


# Greater than  (>)

print(15>5) #Output: True


# Less than  (<)

print(15<5) #Output: False


# greater than or equal to (>=)

print(15>=4) #Output: True


# Smaller than or equal to (<=)

print(15<=4) #Output: False